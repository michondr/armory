const TABLES: Record<DragModel, DragTable> = { G1, G7 };

/** Reference drag coefficient at a given Mach number (linear interpolation, clamped). */
export function dragCoefficient(model: DragModel, mach: number): number {
  const table = TABLES[model];
  const first = table[0]!;
  const last = table[table.length - 1]!;
  if (mach <= first[0]) return first[1];
  if (mach >= last[0]) return last[1];
  // Linear scan is fine: tables are ~80 entries and this runs a few thousand times.
  for (let i = 1; i < table.length; i++) {
    const hi = table[i]!;
    if (mach <= hi[0]) {
      const lo = table[i - 1]!;
      const t = (mach - lo[0]) / (hi[0] - lo[0]);
      return lo[1] + t * (hi[1] - lo[1]);
    }
  }
  return last[1];
}
